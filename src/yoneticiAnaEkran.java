    public void Personel_Sayisi(Statement s){
        try {
            String queryShow = "SELECT SUM(CASE WHEN CINSIYET = 'Erkek' THEN 1 ELSE 0 END) as Erkek_count, SUM(CASE WHEN CINSIYET = 'Kadin' THEN 1 ELSE 0 END) as Kadin_count FROM PERSONEL";
            ResultSet rs = s.executeQuery(queryShow);
            rs.next();
            int countErkek = rs.getInt("Erkek_count");
            int countKadin = rs.getInt("Kadin_count");
            txt_personelIstatistikleriErkekPersonel.setText(Integer.toString(countErkek));
            txt_personelIstatistikleriKadinPersonel.setText(Integer.toString(countKadin));
            rs.close();
        } catch (Exception e) {
        }
    }
