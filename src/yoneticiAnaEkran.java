    private void btn_personelEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_personelEkleActionPerformed

        String isim = "";
        String soyisim = "";
        String Cinsiyet = "";
        String Adres = "";
        Double Maas = 0.0;
        String Unvan = "";
        String Username = "";
        String Password = "";
        
        if (txt_personelEkleName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen isim giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            isim = txt_personelEkleName.getText();
            String patternName = "(?=\\S+$).{1,}";
            Pattern pName = Pattern.compile(patternName);
            Matcher mName = pName.matcher(isim);
            if (!mName.matches()) {
                JOptionPane.showMessageDialog(null, "isim de bosluk bulunamaz!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        if (txt_personelEkleSurname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen soyad giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            soyisim = txt_personelEkleSurname.getText();
            String patternSurName = "(?=\\S+$).{1,}";
            Pattern pSurName = Pattern.compile(patternSurName);
            Matcher mSurName = pSurName.matcher(soyisim);
            if (!mSurName.matches()) {
                JOptionPane.showMessageDialog(null, "soyisim de bosluk bulunamaz!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        if (jComboBox_personelEkleAdres.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen adres seciniz..", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Adres = jComboBox_personelEkleAdres.getSelectedItem().toString();
        }
        
        if ((txt_personelEkleMaas.getText()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen maas giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Maas = Double.parseDouble(txt_personelEkleMaas.getText());
        }
        
        if (jComboBox_PersonelEkleUnvan.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen Unvan seciniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Unvan = jComboBox_PersonelEkleUnvan.getSelectedItem().toString();
        }
        
        if (txt_personelEklePassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen sifre giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Password = txt_personelEklePassword.getText();
            String pattern = "(?=.*[0-9])(?=.*[A-Z])(?=.*[a-z])(?=\\S+$).{6,}";
            Pattern p = Pattern.compile(pattern);
            Matcher m = p.matcher(Password);

            if (!m.matches()) {
                JOptionPane.showMessageDialog(null, "Şifreniz yanlış! Lütfen şifrenizi doğrunuzu giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        if (txt_personelEkleUsarname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen Username giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Username = txt_personelEkleUsarname.getText();
            String pattern = "(?=\\S+$).{1,}";
            Pattern p = Pattern.compile(pattern);
            Matcher m = p.matcher(Username);

            if (!m.matches()) {
                JOptionPane.showMessageDialog(null, "Kullanici adinda bosluk bulunamaz!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
        }

        if(jRadioButton_erkek.isSelected()){
        
            Cinsiyet = "Erkek";
        }
        else{
        
            Cinsiyet = "Kadin";
        }
        
        Connection con;
        try{
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/db1","app","app");
            Statement s = con.createStatement();
  
            String query = "INSERT INTO PERSONEL (NAME,SURNAME,CINSIYET,ADRES,MAAS,UNVAN,USERNAME,PASSWORD) VALUES(?,?,?,?,?,?,?,?) ";
            PreparedStatement pst = con.prepareCall(query);
            pst.setString(1, isim);
            pst.setString(2, soyisim);
            pst.setString(3, Cinsiyet);
            pst.setString(4, Adres);
            pst.setDouble(5, Maas);
            pst.setString(6, Unvan);
            pst.setString(7, Username);
            pst.setString(8, Password);
            pst.executeUpdate();
            con.close();
        }catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_personelEkleActionPerformed

